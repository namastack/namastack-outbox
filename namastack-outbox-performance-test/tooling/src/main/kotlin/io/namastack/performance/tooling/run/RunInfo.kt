package io.namastack.performance.tooling.run

import java.time.Instant

internal data class RunInfo(
    val runId: String,
    val mode: String,
    val profile: String,
    val expectedRecords: Long,
    val distinctKeys: Long,
    val recordsPerKey: Int,
    val consumerInstances: Int,
    val batchSize: Int,
    val pollInterval: String,
    val warmupRecords: Long,
    val status: String,
    val startedAt: Instant?,
    val lastCompletedAt: Instant?,
    val triggerDurationMs: Long?,
    val producerTargetRate: Long?,
    val producerDurationMs: Long?,
    val producerBatchSize: Int?,
    val producerWorkers: Int?,
    val producerStartedAt: Instant?,
    val producerCompletedAt: Instant?,
    val producedRecords: Long?,
    val measurementWarmupMs: Long?,
    val minProducerRateRatio: Double?,
    val maxBacklogGrowthRate: Double?,
    val maxEndBacklog: Long?,
) {
    fun toMap() =
        linkedMapOf(
            "runId" to runId,
            "mode" to mode,
            "profile" to profile,
            "expectedRecords" to expectedRecords,
            "distinctKeys" to distinctKeys,
            "recordsPerKey" to recordsPerKey,
            "consumerInstances" to consumerInstances,
            "batchSize" to batchSize,
            "pollInterval" to pollInterval,
            "warmupRecords" to warmupRecords,
            "status" to status,
            "startedAt" to startedAt?.toString(),
            "lastCompletedAt" to lastCompletedAt?.toString(),
            "triggerDurationMs" to triggerDurationMs,
            "producerTargetRate" to producerTargetRate,
            "producerDurationMs" to producerDurationMs,
            "producerBatchSize" to producerBatchSize,
            "producerWorkers" to producerWorkers,
            "producerStartedAt" to producerStartedAt?.toString(),
            "producerCompletedAt" to producerCompletedAt?.toString(),
            "producedRecords" to producedRecords,
            "measurementWarmupMs" to measurementWarmupMs,
            "minProducerRateRatio" to minProducerRateRatio,
            "maxBacklogGrowthRate" to maxBacklogGrowthRate,
            "maxEndBacklog" to maxEndBacklog,
        )
}
